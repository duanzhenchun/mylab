  `RT_COUNT` int(11) DEFAULT NULL COMMENT '转帖 次数',
  `CT_COUNT` int(11) DEFAULT NULL COMMENT '评论 次数',
  `TYPE` int(11) DEFAULT '1' COMMENT '媒体类型,1:新浪',
  `FAVORITE` int(11) DEFAULT '0' COMMENT '赞数',
  PRIMARY KEY (`STATUS_ID`),
  KEY `ID` (`ID`)
) ENGINE=InnoDB AUTO_INCREMENT=364 DEFAULT CHARSET=utf8;
